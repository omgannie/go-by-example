package main

import "fmt"

func main() {
	// Merge strings w/ +
	fmt.Println("go" + "lang")
	
	// Math
	fmt.Println("1+1=", 1+1)
	fmt.Println("7.0/3.0=", 7.0/3.0)
	
	// Boolean operators are familiar:
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	fmt.Println(!!true)
}